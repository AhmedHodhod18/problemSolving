function telephoneCheck(str) {
  const regex = [
    // 555-555-5555
    /^\d{3}-\d{3}-\d{4}$/,
    // (555)555-5555
    /^\(\d{3}\)\d{3}-\d{4}$/,
    // (555) 555-5555
    /^\(\d{3}\) \d{3}-\d{4}$/,
    // 555 555 5555
    /^\d{3} \d{3} \d{4}$/,
    // 5555555555
    /^\d{10}$/,
    //1 555 555 5555
    /^\d{1} \d{3} \d{3} \d{4}$/,
    // 1 555-555-5555
    /^[1] \d{3}-\d{3}-\d{4}$/,
    //1 (555) 555-5555
    /^[1] \(\d{3}\) \d{3}-\d{4}$/,
    //1(555)555-5555
    /^[1]\(\d{3}\)\d{3}-\d{4}$/
  ]
  return regex.some((x) => x.test(str));
}

console.log(telephoneCheck("1 555-555-5555"));
